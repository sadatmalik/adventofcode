package com.sadatmalik.aoc.daytwentytwo;

public class ReactorGrid {
    int xMin;
    int xMax;

    int yMin;
    int yMax;

    int zMin;
    int zMax;

    public ReactorGrid(int xMin, int xMax, int yMin, int yMax, int zMin, int zMax) {
        this.xMin = xMin;
        this.xMax = xMax;
        this.yMin = yMin;
        this.yMax = yMax;
        this.zMin = zMin;
        this.zMax = zMax;
    }
}
